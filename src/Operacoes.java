public class Operacoes {

    public static void soma (int n1, int n2){
        double resultado =  (n1 + n2);

        System.out.println("A soma de " + n1 + " mais " + n2 + " é igual a " + resultado);

    }

    public static void subtracao (int n1, int n2){
        double resultado =  (n1 - n2);

        System.out.println("A subtração de " + n1 + " menos " + n2 + " é igual a " + resultado);
    }

    public static void multiplicacao (int n1, int n2){
        double resultado = (n1 * n2);

        System.out.println("A multipĺicação de " + n1 + " vezes " + n2 + " é igual a " + resultado);
    }

    public static void divisao (int n1, int n2){
        double resultado = (n1 / n2);

        System.out.println("A divisão de " + n1 + " por " + n2 + " é igual a " + resultado);
    }
}
